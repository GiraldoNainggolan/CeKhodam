function cekKhodam() {
  // Menampilkan loading screen
  const loadingScreen = document.getElementById("loading");
  loadingScreen.style.visibility = "visible";
  loadingScreen.style.opacity = "1";
  loadingScreen.style.transition = "visibility 0s, opacity 0.3s ease-in-out"; // pastikan transisi halus

  // Mengatur waktu tunggu untuk simulasi pemrosesan
  setTimeout(() => {
    // Menyembunyikan loading screen setelah 3 detik
    loadingScreen.style.visibility = "hidden";
    loadingScreen.style.opacity = "0";

    // Memilih nama khodam secara acak
    const randomIndex = Math.floor(Math.random() * khodamNames.length);
    const randomKhodam = khodamNames[randomIndex];

    const nama = document.getElementById("nama").value.trim();

    if (nama === "") {
      alert("Nama tidak boleh kosong!");
      return;
    }

    // Kasus khusus untuk "mbak niyak"
    if (
      nama == "Niyak" ||
      nama == "Utees" ||
      nama == "Niya" ||
      nama == "fudhla" ||
      nama == "Mahbatul" ||
      nama == "Utsniya Fudhla" ||
      nama == "Utsniya Mahbatul Fudhla" ||
      nama == "Utsniya"
    ) {
      const resultContainer = document.getElementById("result");
      resultContainer.innerHTML = `<p class="text-2xl text-white-500 font-bold">"${nama}"</p><br/>
    <p class="text-2xl text-white-500 font-bold">Kodam : Tuyul Ceperet</p>`;
      const inputNama = document.getElementById("nama");
      inputNama.value = "";
      return;
    }

    // Kasus khusus untuk "mbak yayas"
    if (
      nama == "Akyas" ||
      nama == "Yayas" ||
      nama == "Aya" ||
      nama == "Aya'" ||
      nama == "Ayak" ||
      nama == "Akyas mauqiyyah" ||
      nama == "Mauqiyyah"
    ) {
      const resultContainer = document.getElementById("result");
      resultContainer.innerHTML = `<p class="text-2xl text-white-500 font-bold">"${nama}"</p><br/>
    <p class="text-2xl text-white-500 font-bold">Kodam : Kentut Berdahak</p>`;
      const inputNama = document.getElementById("nama");
      inputNama.value = "";
      return;
    }

    // Menampilkan khodam acak jika tidak masuk dalam kategori khusus
    console.log(`Khodam yang muncul: ${randomKhodam}`);
    const resultContainer = document.getElementById("result");
    resultContainer.innerHTML = `<p class="text-2xl text-white-500 font-bold">"${nama}"</p><br/>
    <p class="text-2xl text-white-500 font-bold">Kodam : ${randomKhodam}</p>`;

    const inputNama = document.getElementById("nama");
    inputNama.value = "";
  }, 3000); // Setelah 3 detik, tampilkan hasil
}

// Menangani pengiriman form
const form = document.getElementById("khodamForm");
form.addEventListener("submit", function (event) {
  event.preventDefault(); // Mencegah form dari reload halaman
  cekKhodam();
});
